import { Feather } from "@expo/vector-icons";

type FeatherName = keyof typeof Feather.glyphMap;

/**
 * Tool category names, in display order — identical to the web app's Tools page.
 */
export const TOOL_CATEGORIES = [
  "Convert",
  "Image Tools",
  "Organize",
  "Edit",
  "Video Tools",
] as const;

export type ToolCategory = (typeof TOOL_CATEGORIES)[number];

/** Which interactive editor a tool opens, if any. */
export type EditorKind = "pdf" | "image" | null;

export interface Tool {
  id: string;
  title: string;
  description: string;
  acceptedFormats: string[];
  maxFileSize: string;
  /** Parsed numeric size in MB for client-side validation. */
  maxFileSizeMB: number;
  buttonText: string;
  dropAreaText: string;
  fileTypeHint: string;
  outputFormat: string;
  category: ToolCategory;
  /** Feather icon used as a fallback when the Lottie animation is unavailable. */
  feather: FeatherName;
  /** Web route (kept for reference / parity). Mobile routes use the tool id. */
  route: string;
  /** Server-side tool type (snake_case) for the gated real backend. */
  serverToolType: string;
  /** True when the tool accepts more than one input file. */
  multiFile: boolean;
  /** True for the PDF merge tool. */
  isMerge: boolean;
  /** Which interactive editor the tool opens (else null = generic flow). */
  editor: EditorKind;
  /** Editor sub-mode for the shared PDF editor. */
  editorMode?: string;
  /** True while the tool is temporarily offline ("Under maintenance"). */
  maintenance?: boolean;
  /** Short badge/title shown when offline (defaults to "Under maintenance"). */
  maintenanceLabel?: string;
  /** Longer explanation shown on the tool's offline screen. */
  maintenanceNote?: string;
  /** True when the tool offers a "Take Photo" (camera) capture option. */
  allowCamera?: boolean;
}

function mb(size: string): number {
  const n = parseInt(size.replace(/[^0-9]/g, ""), 10);
  return Number.isFinite(n) ? n : 50;
}

